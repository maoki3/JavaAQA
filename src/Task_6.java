public class Task_6 {
    public static void negativeOrPositive(int number) {

        if (number >= 0) {
            System.out.println("Положительное число");
        } else {
            System.out.println("Отрицательное число");

        }
    }

    public static void main(String[] args) {

        negativeOrPositive(-6);
    }

}
